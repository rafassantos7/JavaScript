function somar(a,b){
    return a+b
}
function subtrair(a,b){
    return a-b
}
function multiplicar(a,b){
    return a*b
}
function dividir(a,b){
    return a/b
}



const soma = somar(2,3)
const sub = subtrair(12,3)
const div = dividir(10,2)
const mult = multiplicar(10,2)

console.log(`Soma: ${soma}`)
console.log(`Subtração: ${sub}`)
console.log(`Multiplicacao: ${div}`)
console.log(`Divisao: ${mult}`)